const ScientistSection = ({ scientist }: { scientist: IScientist }) => {
    const [expanded, setExpanded] = useState(false)

    return (
        <div className={styles.section}>
            <button
                className={`${styles.section_toggle} ${expanded ? styles.section_toggle_open : ''}`}
                onClick={() => setExpanded(prev => !prev)}
            >
                <div className={styles.section_toggle_left}>
                    <User size={18} className={styles.section_icon} />
                    <span className={styles.section_name}>{scientist.name}</span>
                </div>
                <div className={styles.section_toggle_right}>
                    <span className={styles.section_count}>
                        <BookOpen size={13} />
                        {scientist.publications.length > 0
                            ? `${scientist.publications.length} публікацій`
                            : 'Профіль'}
                    </span>
                    <ChevronDown
                        size={18}
                        className={`${styles.chevron} ${expanded ? styles.chevron_open : ''}`}
                    />
                </div>
            </button>

            <div className={`${styles.section_content} ${expanded ? styles.section_content_open : ''}`}>
                <div className={styles.section_inner}>

                    <ol className={styles.pub_list}>
                        {scientist.publications.map((pub, i) => (
                            <li key={i} className={styles.pub_item}>
                                <span>{pub.text}</span>
                                {pub.url && (
                                    <a
                                        href={pub.url}
                                        target="_blank"
                                        rel="noopener noreferrer"
                                        className={styles.pub_link}
                                    >
                                        <ExternalLink size={12} />
                                        DOI
                                    </a>
                                )}
                            </li>
                        ))}
                    </ol>

                    {scientist.profiles && scientist.profiles.length > 0 && (
                        <div className={styles.profiles}>
                            <p className={styles.profiles_label}>Профілі наукових праць:</p>
                            <div className={styles.profiles_list}>
                                {scientist.profiles.map((profile) => (
                                    <a
                                        key={profile.url}
                                        href={profile.url}
                                        target="_blank"
                                        rel="noopener noreferrer"
                                        className={styles.profile_link}
                                    >
                                        <ExternalLink size={14} />
                                        {profile.label}
                                    </a>
                                ))}
                            </div>
                        </div>
                    )}

                </div>
            </div>
        </div>
    )
}

